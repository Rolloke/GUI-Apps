#ifndef __HACCIOCTL_H__
#define __HACCIOCTL_H__

typedef USHORT			WORD;
typedef ULONG			DWORD;
typedef unsigned char	BYTE;
typedef int				BOOL;
typedef ULONG			HIRQ;

// Device type           -- in the "User Defined" range."
#define HACC_TYPE 40000

// The IOCTL function codes from 0x800 to 0xFFF are for customer use.
#define HACC_OPEN							CTL_CODE(HACC_TYPE, 0x900, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_CLOSE							CTL_CODE(HACC_TYPE, 0x901, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_GET_DRIVERVERSION				CTL_CODE(HACC_TYPE, 0x902, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_MAP_PHYS_TO_LIN				CTL_CODE(HACC_TYPE, 0x903, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_UNMAP_PHYS_TO_LIN				CTL_CODE(HACC_TYPE, 0x904, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_MAP_LIN_TO_PHYS				CTL_CODE(HACC_TYPE, 0x905, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_WRITE_PORT						CTL_CODE(HACC_TYPE, 0x906, METHOD_BUFFERED, FILE_WRITE_ACCESS)
#define HACC_READ_PORT						CTL_CODE(HACC_TYPE, 0x907, METHOD_BUFFERED, FILE_READ_ACCESS)
#define HACC_WRITE_MEMORY					CTL_CODE(HACC_TYPE, 0x908, METHOD_BUFFERED, FILE_WRITE_ACCESS)
#define HACC_READ_MEMORY					CTL_CODE(HACC_TYPE, 0x909, METHOD_BUFFERED, FILE_READ_ACCESS)
#define HACC_READ_EVENT_PACKET				CTL_CODE(HACC_TYPE, 0x90a, METHOD_BUFFERED, FILE_READ_ACCESS)
#define HACC_READ_QUEUE_STATE				CTL_CODE(HACC_TYPE, 0x90b, METHOD_BUFFERED, FILE_READ_ACCESS)
#define HACC_ALLOC_MEMORY					CTL_CODE(HACC_TYPE, 0x90c, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_FREE_MEMORY					CTL_CODE(HACC_TYPE, 0x90d, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
#define HACC_GET_FRAME_BUFFER				CTL_CODE(HACC_TYPE, 0x90e, METHOD_BUFFERED, FILE_READ_ACCESS | FILE_WRITE_ACCESS)
 
#endif // __HACCIOCTL_H__

