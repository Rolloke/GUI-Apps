#include "stdafx.h"
#include "CMyDebug.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////
CMyDebug::CMyDebug()
{

}

//////////////////////////////////////////////////////////////////////
CMyDebug::~CMyDebug()
{
}

//////////////////////////////////////////////////////////////////////
void CMyDebug::OnReceivedMessage(const CString& sMessage) const
{
	WK_TRACE(sMessage);
}

//////////////////////////////////////////////////////////////////////
void CMyDebug::OnReceivedWarning(const CString& sWarning) const
{
	WK_TRACE_WARNING(sWarning);
}

//////////////////////////////////////////////////////////////////////
void CMyDebug::OnReceivedError(const CString& sError) const
{
	WK_TRACE_ERROR(sError);
}

//////////////////////////////////////////////////////////////////////
void CMyDebug::OnReceivedStatLog(LPCTSTR szTopic, int iValue, LPCTSTR sName) const
{
	WK_STAT_LOG(szTopic, iValue, sName);
}

//////////////////////////////////////////////////////////////////////
void CMyDebug::OnReceivedStatPeak(LPCTSTR szTopic, int iValue, LPCTSTR sName) const
{
	WK_STAT_PEAK(szTopic, iValue, sName);
}
