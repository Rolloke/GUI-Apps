#include "stdafx.h"
#include "CIOPort.h"
#include "..\\SavicDA\\SavicDirectAccess.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////
CIoPort::CIoPort(DWORD dwIOBase) : CIo(dwIOBase)
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
CIoPort::~CIoPort()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
BYTE CIoPort::Input8(DWORD dwPort) const
{
	return DAReadPort8(dwPort);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
WORD CIoPort::Input16(DWORD dwPort) const
{
	return DAReadPort16(dwPort);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
DWORD CIoPort::Input32(DWORD dwPort) const
{
	return DAReadPort32(dwPort);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void CIoPort::Output8(DWORD dwPort, BYTE byData) const
{
	DAWritePort8(dwPort, byData);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void CIoPort::Output16(DWORD dwPort, WORD wData) const 
{
	DAWritePort16(dwPort, wData);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void CIoPort::Output32(DWORD dwPort, DWORD dwData) const 
{
	DAWritePort32(dwPort, dwData);
}

