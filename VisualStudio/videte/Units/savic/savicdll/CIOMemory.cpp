#include "stdafx.h"
#include "CIOMemory.h"
#include "..\\SavicDA\\SavicDirectAccess.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////
CIoMemory::CIoMemory(DWORD dwIOBase) : CIo(dwIOBase)
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
CIoMemory::~CIoMemory()
{
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
BYTE CIoMemory::Input8(DWORD dwPort) const
{
	return DAReadMemory8(dwPort);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
WORD CIoMemory::Input16(DWORD dwPort) const
{
	return DAReadMemory16(dwPort);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
DWORD CIoMemory::Input32(DWORD dwPort) const
{
	return DAReadMemory32(dwPort);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void CIoMemory::Output8(DWORD dwPort, BYTE byData) const
{
	DAWriteMemory8(dwPort, byData);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void CIoMemory::Output16(DWORD dwPort, WORD wData) const 
{
	DAWriteMemory16(dwPort, wData);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void CIoMemory::Output32(DWORD dwPort, DWORD dwData) const 
{
	DAWriteMemory32(dwPort, dwData);
}

