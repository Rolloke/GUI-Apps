//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////
CValue::CValue(int nValue, int nField, int nSyncCounter, COLORREF col)
{
	m_nValue		= nValue;
	m_col			= col;
	m_nField		= nField;
	m_nSyncCounter	= nSyncCounter;
}

//////////////////////////////////////////////////////////////////////
CValue::CValue()
{
	m_nValue		= 0;
	m_col			= RGB(0,0,0);
	m_nField		= 0;
	m_nSyncCounter	= 0;
}

//////////////////////////////////////////////////////////////////////
CValue::~CValue()
{

}

//////////////////////////////////////////////////////////////////////
int CValue::GetInt()
{
	return m_nValue;
}

//////////////////////////////////////////////////////////////////////
COLORREF CValue::GetColor()
{
	return m_col;
}

//////////////////////////////////////////////////////////////////////
int CValue::GetField()
{
	return m_nField;
}

//////////////////////////////////////////////////////////////////////
int CValue::GetSyncCounter()
{
	return m_nSyncCounter;
}
